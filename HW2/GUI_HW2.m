% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)

runcode(handles, hObject) 

function runcode(handles, hObject) 
    clearvars -except handles

    guihw = 1;

    lambda = str2num(get(handles.edit1, 'String'));
    ny = str2num(get(handles.edit2, 'String'));
    nu = str2num(get(handles.edit3, 'String'));
    simhor = str2num(get(handles.edit4, 'String'));
    UMAX = str2num(get(handles.edit5, 'String'));

    switch get(handles.listbox1,'value')
        case 1
            try
                if handles.holdon || ~get(handles.radiobutton1, 'Value')
                    set(handles.figure1, 'HandleVisibility', 'off');
                    close all
                    set(handles.figure1, 'HandleVisibility', 'on');
                    global fig1;
                    fig1 = figure('Name','Question 1a - MPC WITHOUT Disturbance Compensation');    
                else
                    global fig1
                    set(0, 'currentfigure', fig1);
                end

                set(handles.text8, 'String', 'running ...');
                NoDistCompAssigned
                
                if sol
                    set(handles.text8, 'String', 'Concluded');
                else
                    set(handles.text8, 'String', 'Solution Not Found');
                    set(handles.figure1, 'HandleVisibility', 'off');
                    close all
                    set(handles.figure1, 'HandleVisibility', 'on');                   
                    f = msgbox('Solution Not Found', 'Error','error');
                end               
            end
        case 2
            try
                if handles.holdon || ~get(handles.radiobutton1, 'Value')
                    set(handles.figure1, 'HandleVisibility', 'off');
                    close all
                    set(handles.figure1, 'HandleVisibility', 'on');
                    global fig1;
                    fig1 = figure('Name','Question 1b - MPC WITH Disturbance Compensation');    
                else
                    global fig1
                    set(0, 'currentfigure', fig1);
                end

                set(handles.text8, 'String', 'running ...');
                DistCompAssigned
                
                if sol
                    set(handles.text8, 'String', 'Concluded');
                else
                    set(handles.text8, 'String', 'Solution Not Found');
                    set(handles.figure1, 'HandleVisibility', 'off');
                    close all
                    set(handles.figure1, 'HandleVisibility', 'on');                   
                    f = msgbox('Solution Not Found', 'Error','error');
                end               
            end
        case 3
            try 
                if handles.holdon || ~get(handles.radiobutton1, 'Value')
                    set(handles.figure1, 'HandleVisibility', 'off');
                    close all
                    set(handles.figure1, 'HandleVisibility', 'on');
                    global fig1;
                    fig1 = figure('Name','Question 2 - Elevator Control');    
                else
                    global fig1
                    set(0, 'currentfigure', fig1);
                end
                MPCelevatorCaseStudyAssigned
            catch
            end
    end
    
